fn solve(s: &mut String) {
    read_line(s);
    let mut arr: Vec<u64> = read_arr(s);
    let mut r = 0;
    for elt in &mut arr {
        if *elt > 4 {
            *elt = 0;
            r += 1;
        } else {
            *elt = elt.div_ceil(2);
        }
    }
    // println!("arr: {arr:?}");
    for i in 0..arr.len() {
        if i == arr.len() - 1 {
            if arr[i] != 0 {
                r += 1;
            }
            continue;
        }
        let elt = arr[i];
        if elt == 0 {
            continue;
        }
        let next = arr[i + 1];

        if elt == 1 && next == 1 {
            arr[i] = 0;
            arr[i + 1] = 0;
            r += 1;
            continue;
        }
        if elt == 1 && next == 2 {
            let mut k = 0;
            loop {
                let j = i + k * 2 + 1;
                if j >= arr.len() - 1 {
                    break;
                }
                let a = arr[j];
                let b = arr[j + 1];

                if a == 0 || b == 0 {
                    break;
                }
                if a == 1 {
                    break;
                }
                k += 1;
            }
            // println!("k: {k}");
            let j = i + k * 2 + 1;
            if j > arr.len() - 1 || arr[j] != 1 {
                arr[i] = 0;
                arr[i + 1] = 0;
                r += 2;
                continue;
            }

            for it in i..=j {
                arr[it] = 0;
                r += 1;
            }
            r -= 1;
            continue;
        }
        r += 1;
    }
    result!("{r}");
}
fn main() {
    let mut s = String::new();

    let [n_t] = read_vals(&mut s);

    for _ in 0..n_t {
        solve(&mut s);
    }
}
