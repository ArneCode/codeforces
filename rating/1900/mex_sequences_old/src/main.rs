fn solve(s: &mut String) {
    read_line(s);
    let a: Vec<usize> = read_arr(s);

    let mut idx = vec![vec![]; a.len() + 1];
    for (i, v) in a.iter().enumerate() {
        idx[*v].push(i);
    }

    let mut dp = vec![0; a.len()];

    let mut dp_sums = vec![vec![]; a.len() + 1];

    let mut end = vec![0; a.len()];

    for v in 0..=a.len() {
        let idxs = &idx[v];

        let mut p_i = 0;
        dp_sums[v].push(0);

        let mut dp_sum = 0;
        for i in idxs {
            if v == 0 {
                dp[*i] += 1;
            } else {
                while p_i < idx[v - 1].len() && idx[v - 1][p_i] < *i {
                    p_i += 1;
                }
                //println!("{} {}", idx[v - 1].len(), dp_sums[v - 1].len());
                dp[*i] = dp_sums[v - 1][p_i];
            }
            dp_sum += dp[*i];

            dp_sums[v].push(dp_sum);
        }

        let mut p_i = 0;

        for i in idxs {
            while p_i < idx[v].len() && idx[v][p_i] < *i {
                p_i += 1;
            }
            end[*i] += dp_sums[v][p_i];
        }

        if v > 1 {
            let mut p_i = 0;

            for i in idxs {
                while p_i < idx[v - 2].len() && idx[v - 2][p_i] < *i {
                    p_i += 1;
                }
                end[*i] += dp_sums[v - 2][p_i];
            }
        }
    }
    println!("dp: {dp:?}, end: {end:?}");
    let mut r: u64 = dp.iter().sum();
    r += end.iter().sum::<u64>();

    result!("{r}");
}
fn main() {
    let mut s = String::new();

    let [n_t] = read_vals(&mut s);

    for _ in 0..n_t {
        solve(&mut s);
    }
}
