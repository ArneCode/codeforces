fn solve(s: &mut String) {
    let [n, k, p_b, p_s] = read_vals_t(s, 0usize);
    let p_b = p_b - 1;
    let p_s = p_s - 1;

    let mut ps: Vec<usize> = read_arr(s);
    for v in ps.iter_mut() {
        *v -= 1;
    }
    let a_s: Vec<u64> = read_arr(s);

    let mut path_b = vec![p_b];
    loop {
        let curr = path_b.last().unwrap();
        let next = ps[*curr];
        if next == path_b[0] {
            break;
        }
        path_b.push(next);
    }
    let mut path_s = vec![p_s];
    loop {
        let curr = path_s.last().unwrap();
        let next = ps[*curr];
        if next == path_s[0] {
            break;
        }
        path_s.push(next);
    }

    // println!("path_b: {path_b:?}");
    // println!("path_s: {path_s:?}");

    let mut scores_b = vec![];
    let mut sum = 0;
    for (i, pos) in path_b.into_iter().enumerate() {
        let elt = a_s[pos];
        
        if k < i {
            break;
        }
        let time = (k - i) as u64;
        scores_b.push(sum + time*elt);

        sum += elt;
    }

    let score_b = *scores_b.iter().max().unwrap();

    let mut scores_s = vec![];
    let mut sum = 0;
    for (i, pos) in path_s.into_iter().enumerate() {
        let elt = a_s[pos];
        
        if k < i {
            break;
        }
        let time = (k - i) as u64;
        scores_s.push(sum + time*elt);

        sum += elt;
    }

    let score_s = *scores_s.iter().max().unwrap();

    if score_b > score_s {
        println!("Bodya");
    }else if score_s > score_b {
        println!("Sasha");
    }else{
        println!("Draw");
    }
}
fn main() {
    let mut s = String::new();

    let [n_t] = read_vals(&mut s);

    for _ in 0..n_t {
        solve(&mut s);
    }
}
